#!/usr/bin/env bash
#                    __
#  _    _____ ___ __/ /  ___ _____
# | |/|/ / _ `/ // / _ \/ _ `/ __/
# |__,__/\_,_/\_, /_.__/\_,_/_/
#            /___/
#

# -----------------------------------------------------
# Prevent duplicate launches: only the first parallel
# invocation proceeds; all others exit immediately.
# -----------------------------------------------------

exec 200>/tmp/waybar-launch.lock
flock -n 200 || exit 0

# -----------------------------------------------------
# Quit all running waybar instances
# -----------------------------------------------------

killall waybar || true
pkill waybar || true
sleep 0.5

# -----------------------------------------------------
# Loading the configuration
# -----------------------------------------------------

if pgrep -x "Hyprland" >/dev/null ; then
    config_file="config.hypr.jsonc"
    style_file="style.hypr.css"
elif pgrep -x "niri" >/dev/null ; then
    config_file="config.niri.jsonc"
    style_file="style.niri.css"
elif pgrep -x "mango" >/dev/null ; then
    config_file="config.mango.jsonc"
    style_file="style.mango.css"
fi

if pgrep -x "Hyprland" >/dev/null ; then
    HYPRLAND_SIGNATURE=$(hyprctl instances -j | jq -r '.[0].instance')
    HYPRLAND_INSTANCE_SIGNATURE="$HYPRLAND_SIGNATURE" waybar -c ~/.config/waybar/$config_file -s ~/.config/waybar/$style_file &
else
    waybar -c ~/.config/waybar/$config_file -s ~/.config/waybar/$style_file &
fi
# env GTK_DEBUG=interactive waybar -c ~/.config/waybar/$config_file -s ~/.config/waybar/$style_file &

# Explicitly release the lock (optional) -> flock releases on exit
flock -u 200
exec 200>&-